namespace CTC.DataBind.Formatter
{
	/// <summary>타이머 숫자 표시 타입입니다.</summary>
	public enum TimerFormat
	{
		/// <summary>
		/// 소수점을 표시합니다.
		/// </summary>
		Decimal,

		/// <summary>
		/// 0을 표시합니다.
		/// </summary>
		DisplayZero,

		/// <summary>
		/// 0을 표시하지 않습니다.
		/// </summary>
		NoDisplayZero,
	}
}
